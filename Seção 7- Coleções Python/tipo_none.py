"""
Tipo none

O tipo de dado none em python representa o tipo sem tipo, ou poderia ser conhecido também
como tipo vazio, porém, falar que é um tipo sem tipo é mais apropriado.

OBS: O tipo None é SEMPRE especificado com primeira letra maiuscula.

Certo: None
Errado: none

Quanbdo utilizamos?

- Podemos utilizar None quando queremos criar uma variavel e inicializa-la com um tipo sem tipo, antes
de recebermos um valor final.

OBS: O tipo None em python é sempre considerado falso
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.34

print(numeros)
print(type(numeros))