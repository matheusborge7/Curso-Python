"""
2- Faça um programa que peça para o usuario digitar três valores inteiros e imprima a soma deles.
"""

numero1: int = int(input("Informe o primeiro número inteiro: "))
numero2: int = int(input("Informe o segundo número inteiro: "))
numero3: int = int(input("Informe o terceiro número inteiro: "))

soma: int= numero1 + numero2 + numero3
print(f"A soma dos números {numero1} com {numero2} e {numero3} é: {soma}")